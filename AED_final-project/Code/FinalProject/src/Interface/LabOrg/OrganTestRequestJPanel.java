    public void populateOBRequestTable(){
        DefaultTableModel dtm = (DefaultTableModel)tbl_organBankrequest.getModel();
        dtm.setRowCount(0);
        for(WorkRequest request : organization.getWorkQueue().getWorkRequestList())
        {
            if(request.getReceiver()==null)
            {
                Object[] row = new Object[4];
                row[0] = request.getOrganbankName();
                row[1] = request.getDonar().getDonarID();
                row[2] = request.getDonar().getAge();
                row[3] = request;

                dtm.addRow(row);
            }
        }
    }
    
    public void populateUserTable()
    {
        DefaultTableModel model = (DefaultTableModel)tbl_userRequest.getModel();
        model.setRowCount(0);
        for(WorkRequest request : userAccount.getWorkQueue().getWorkRequestList())
        {
            Object[] row = new Object[8];
            row[0]= request.getOrganbankName();
            row[1] = request.getDonar().getDonarID();
            row[2] = request.getDonar().getAge();
            row[3] = request.getSender();
            row[4] = request.getStatus();
            row[5] = request;
            model.addRow(row);
         }
    }

