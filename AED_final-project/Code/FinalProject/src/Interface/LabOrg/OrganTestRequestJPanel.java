    private void btn_AssignActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_AssignActionPerformed
        // TODO add your handling code here:
        int selectedRow = tbl_organBankrequest.getSelectedRow();

        if (selectedRow < 0){
            JOptionPane.showMessageDialog(null, "Please select a row");
            return;
        }
        
        LabWorkRequest LabWorkRequest = (LabWorkRequest) tbl_organBankrequest.getValueAt(selectedRow,3);
        LabWorkRequest.setReceiver(userAccount);
        LabWorkRequest.setStatus("work assigned to " +userAccount);
        LabWorkRequest.setMessage("pending with " + userAccount);

            //org.getWorkQueue().getWorkRequestList().add(organBankWorkRequest);
            userAccount.getWorkQueue().getWorkRequestList().add(LabWorkRequest);
            //JOptionPane.showMessageDialog(null,"Request has been assigned");

        populateOBRequestTable();
        populateUserTable();
    }//GEN-LAST:event_btn_AssignActionPerformed

    private void btn_processBodyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_processBodyActionPerformed
        // TODO add your handling code here:
        int selectedRow = tbl_userRequest.getSelectedRow();

        if (selectedRow < 0){
            JOptionPane.showMessageDialog(null, "Please select a row");
            return;
        }

        LabWorkRequest request = (LabWorkRequest) tbl_userRequest.getValueAt(selectedRow,5);
        if(!(request.getStatus()=="completed"))
        {
            UpdateOrganJPanel updateOrganJPanel = new UpdateOrganJPanel(userProcessContainer, userAccount, organization, enterprise, business, request);
            userProcessContainer.add("UpdateOrganJPanel", updateOrganJPanel);
            CardLayout layout = (CardLayout) userProcessContainer.getLayout();
            layout.next(userProcessContainer);
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Body already processed ");
            return;
        }

    }//GEN-LAST:event_btn_processBodyActionPerformed

