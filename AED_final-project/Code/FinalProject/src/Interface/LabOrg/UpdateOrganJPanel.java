        // TODO add your handling code here:
        if(chackValues())
        {
            int kidneygood = Integer.parseInt(txt_kidneyGood.getText());
            int kidneybad=Integer.parseInt(txt_kidneybad.getText());
            int kidneyNa=Integer.parseInt(txt_kidneyNA.getText());

            int livergood = Integer.parseInt(txt_livergood.getText());
            int liverbad=Integer.parseInt(txt_liverBad.getText());
            int liverNa=Integer.parseInt(txt_liverNA.getText());

            int eyesgood = Integer.parseInt(txt_Eyesgood.getText());
            int eyesbad=Integer.parseInt(txt_EyesBad.getText());
            int eyesNa=Integer.parseInt(txt_EyesNA.getText());

            int heartgood = Integer.parseInt(txt_HeartGood.getText());
            int heartbad=Integer.parseInt(txt_HeartBad.getText());
            
            Donar d = request.getDonar();
            Organ kid = d.addorgan();
            kid.setOrganName(txt_kidney.getText());
            kid.setQuantity(kidneygood);
            kid.setCondition("Good");
            
            Organ liver = d.addorgan();
            liver.setOrganName(txt_liver.getText());
            liver.setQuantity(livergood);
            liver.setCondition("Good");
            
            Organ Eyes = d.addorgan();
            Eyes.setOrganName(txt_eyes.getText());
            Eyes.setQuantity(eyesgood);
            Eyes.setCondition("Good");
            
            Organ heart = d.addorgan();
            heart.setOrganName(txt_heart.getText());
            heart.setQuantity(heartgood);
            heart.setCondition("Good");
            organization.getLabOrganInventory().addGoodOrgans(d);
            
            
            Donar dbad = request.getDonar();
            Organ kid_bad = dbad.addorgan();
            kid_bad.setOrganName(txt_kidney.getText());
            kid_bad.setQuantity(kidneybad);
            kid_bad.setCondition("Bad");
            
            Organ livrbad = dbad.addorgan();
            livrbad.setOrganName(txt_liver.getText());
            livrbad.setQuantity(liverbad);
            livrbad.setCondition("Bad");
            
            Organ Eyebad = dbad.addorgan();
            Eyebad.setOrganName(txt_eyes.getText());
            Eyebad.setQuantity(eyesbad);
            Eyebad.setCondition("Bad");
            
            Organ hertbad = dbad.addorgan();
            hertbad.setOrganName(txt_heart.getText());
            hertbad.setQuantity(heartbad);
            hertbad.setCondition("Bad");
            organization.getLabOrganInventory().addBadOrgans(dbad);
            
            Donar dNA = request.getDonar();
            Organ kid_NA = dNA.addorgan();
            kid_NA.setOrganName(txt_kidney.getText());
            kid_NA.setQuantity(kidneyNa);
            kid_NA.setCondition("NA");
            
            Organ livrNA = dNA.addorgan();
            livrNA.setOrganName(txt_liver.getText());
            livrNA.setQuantity(liverNa);
            livrNA.setCondition("NA");
            
            Organ EyeNA = dNA.addorgan();
            EyeNA.setOrganName(txt_eyes.getText());
            EyeNA.setQuantity(eyesNa);
            EyeNA.setCondition("NA");
            
            
            organization.getLabOrganInventory().addNaOrgans(dNA);
            
            for(Network net : business.getNetworkList())
            {
                for(Enterprise ent : net.getEnterpriseDirectory().getEnterpriseDirectory())
                {
                    for(Organization org : ent.getOrganizationDirectory().getOrganizationList())
                    {
                        if(org instanceof OrganBankOrganization)
                        {
                            for(UserAccount ua :org.getUserAccountDirectory().getUserAccountList())
                            {
                                if(ua == request.getSender())
                                {
                                    for(WorkRequest wr : ua.getWorkQueue().getWorkRequestList())
                                    {
                                        if(wr.getDonar().getDonarID() == request.getDonar().getDonarID())
                                        {
                                            wr.setLabStatus("completed");
                                            wr.setDonar(d);
                                            wr.setStatus("Test completed");
                                            wr.setMessage("Update Inventory");
                                            request.setStatus("completed");
                                            request.setMessage("testing completed");
                                        }
                                    }
                                }
                            }
                        }
                    }
                }
            }
            
            userProcessContainer.remove(this);
            Component[] componentArray = userProcessContainer.getComponents();
            Component component = componentArray[componentArray.length - 1];
            OrganTestRequestJPanel organTestRequestJPanel = (OrganTestRequestJPanel) component;
            organTestRequestJPanel.populateUserTable();
            CardLayout layout = (CardLayout)userProcessContainer.getLayout();
            layout.previous(userProcessContainer);
        }   
