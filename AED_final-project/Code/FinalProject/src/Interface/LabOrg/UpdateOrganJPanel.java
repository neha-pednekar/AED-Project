    public boolean chackValues()
    {
        boolean stat= true;
        try
        {
            int kidneygood = Integer.parseInt(txt_kidneyGood.getText());
            int kidneybad=Integer.parseInt(txt_kidneybad.getText());
            int kidneyNa=Integer.parseInt(txt_kidneyNA.getText());

            int livergood = Integer.parseInt(txt_livergood.getText());
            int liverbad=Integer.parseInt(txt_liverBad.getText());
            int liverNa=Integer.parseInt(txt_liverNA.getText());

            int eyesgood = Integer.parseInt(txt_Eyesgood.getText());
            int eyesbad=Integer.parseInt(txt_EyesBad.getText());
            int eyesNa=Integer.parseInt(txt_EyesNA.getText());

            int heartgood = Integer.parseInt(txt_HeartGood.getText());
            int heartbad=Integer.parseInt(txt_HeartBad.getText());

            int kidneyQuantity = kidneygood+kidneybad+kidneyNa;
            int liverquantity = livergood+liverbad+liverNa;
            int eyesquantity = eyesgood+eyesbad+eyesNa;
            int heartQuantity = heartgood+heartbad;
            
            
            if(kidneyQuantity!=2)
            {
                JOptionPane.showMessageDialog(null, "Kidney quantity not filled properly");
                stat = false;
            }
            else if(liverquantity!=2)
            {
                JOptionPane.showMessageDialog(null, "Liver quantity not filled properly");
                stat = false;
            }
            else if(eyesquantity!=2)
            {
                JOptionPane.showMessageDialog(null, "Eyes quantity not filled properly");
                stat = false;
            }
            else if(heartQuantity!=1)
            {
                JOptionPane.showMessageDialog(null, "Heart quantity not filled properly");
                stat = false;
            }
        }
        catch(NumberFormatException ex)
        {
            JOptionPane.showMessageDialog(null, "Please enter the values properly");
            stat = false;
        }
        return stat;
    }

