    private void btn_selectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_selectActionPerformed
        // TODO add your handling code here:
        LabOrganization org = (LabOrganization)cb_labName.getSelectedItem();
        request.setLabName(org.getOrganizationName());
        request.setLabStatus("pending");
        request.setSender(userAccount);
        
        LabWorkRequest labrequest = new LabWorkRequest();
        labrequest.setDonar( request.getDonar()); 
        labrequest.setOrganbankName(organization.getOrganizationName()); 
        labrequest.setLabName(org.getOrganizationName()); 
        labrequest.setSender( userAccount);
        labrequest.setMessage("Please test the organs");
        
        org.getWorkQueue().getWorkRequestList().add(labrequest);
        
        JOptionPane.showMessageDialog(null, org.getName()+" has been assigned the organ testing");
        
        userProcessContainer.remove(this);
        Component[] componentArray = userProcessContainer.getComponents();
        Component component = componentArray[componentArray.length - 1];
        OrganSystemWorkRequestJPanel organSystemWorkRequestJPanel = (OrganSystemWorkRequestJPanel) component;
        organSystemWorkRequestJPanel.populateUserTable();
        CardLayout layout = (CardLayout)userProcessContainer.getLayout();
        layout.previous(userProcessContainer);
        
        
    }//GEN-LAST:event_btn_selectActionPerformed

