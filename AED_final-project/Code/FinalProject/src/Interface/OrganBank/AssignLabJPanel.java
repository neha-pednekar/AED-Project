    public void populateLabName()
    {
        for (Network network : business.getNetworkList())
        {
            for(Enterprise en : network.getEnterpriseDirectory().getEnterpriseDirectory())
            {
                for(Organization org : en.getOrganizationDirectory().getOrganizationList())
                {
                    if(org instanceof LabOrganization)
                    {
                        cb_labName.addItem(org);
                    }
                }
            }
        }
    }

