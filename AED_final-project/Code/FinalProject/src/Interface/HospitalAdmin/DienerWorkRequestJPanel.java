    public void populatefeilds()
    {
        Donar donar = request.getPatient().getDonor();
        txt_PID.setText(String.valueOf(donar.getDonarID()));
        txt_name.setText(donar.getName());
        txt_age.setText(String.valueOf(donar.getAge()));
        txt_address.setText(donar.getAddress());
        txt_zipcode.setText(String.valueOf(donar.getZipCode()));
        txt_contactNo.setText(donar.getContactNo());
        txt_emgContactNo.setText(donar.getEmgContantno());
        cb_gender.setSelectedItem(donar.getGender());
        txt_datetime.setText(String.valueOf(new Date()));
        boolean donate = donar.isDonate();
        if(donate)
        rb_yes.setSelected(true);
        else
            rb_no.setSelected(true);
        
    }

