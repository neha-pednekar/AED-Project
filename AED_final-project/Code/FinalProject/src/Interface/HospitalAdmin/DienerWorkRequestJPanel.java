    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        Donar donar = request.getPatient().getDonor();
        donar.setDied(true);
        
        Deceased deceased = business.addDeceased();
        deceased.setDonar(donar);
        deceased.setHospitalName(organization.getOrganizationName());
        deceased.setAge(donar.getAge());
        
        request.setTestResult("Issued");
        request.setStatus("Completed");
        request.setMessage("Issued");
        
        JOptionPane.showMessageDialog(null,"Certificate Issued");
        userProcessContainer.remove(this);
        Component[] componentArray = userProcessContainer.getComponents();
        Component component = componentArray[componentArray.length - 1];
        DienerManageRequestJPanel dienerManageRequestJPanel = (DienerManageRequestJPanel) component;
        dienerManageRequestJPanel.populateUserTable();
        dienerManageRequestJPanel.populateTable();
        
        CardLayout layout = (CardLayout)userProcessContainer.getLayout();
        layout.previous(userProcessContainer);
        
    }//GEN-LAST:event_jButton1ActionPerformed

