    private void assignJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_assignJButtonActionPerformed

        int selectedRow = workRequestJTable.getSelectedRow();

        if (selectedRow < 0){
            JOptionPane.showMessageDialog(null, "Please select a row");
            return;
        }

        WorkRequest request = (WorkRequest)workRequestJTable.getValueAt(selectedRow, 6);
        request.setReceiver(userAccount);
        request.setStatus("Pending");
        
        populateTable();

    }//GEN-LAST:event_assignJButtonActionPerformed

    private void processJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_processJButtonActionPerformed

        int selectedRow = workRequestJTable.getSelectedRow();

        if (selectedRow < 0){
            return;
        }

        HospitalWorkRequest request = (HospitalWorkRequest)workRequestJTable.getValueAt(selectedRow, 6);

        request.setStatus("Processing");
        
        String requiredOrganName = request.getOrganName();
        int quantity = request.getQuantity();
        

        ProcessOrganRequestJPanel processOrganRequestJPanel = new ProcessOrganRequestJPanel(userProcessContainer, userAccount,
        enterprise, hospitalOrganization, business, requiredOrganName, quantity, request);
        userProcessContainer.add("processOrganRequestJPanel", processOrganRequestJPanel);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.next(userProcessContainer);

    }//GEN-LAST:event_processJButtonActionPerformed

