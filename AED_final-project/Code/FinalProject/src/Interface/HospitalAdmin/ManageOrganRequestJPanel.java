    public void populateTable(){
        DefaultTableModel tableModel = (DefaultTableModel)workRequestJTable.getModel();
        tableModel.setRowCount(0);
        
        for(WorkRequest request : hospitalOrganization.getWorkQueue().getWorkRequestList())
        {
            if(request.getType().equalsIgnoreCase("Admin"))
            {
            Object[] row = new Object[7];
            row[0] = request.getOrganName();
            row[1] = request.getPatient();
            row[2] = request.getQuantity();
            row[3] = request.getReceiver();
            row[4] = request.getSender();
            row[5] = request.getStatus();
            row[6] = request;
            
            tableModel.addRow(row);
            }
        }
    }

